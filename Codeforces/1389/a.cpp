void solve() {
    int l, r; cin >> l >> r;
    if (r >= l * 2) {
        cout << l << " " << 2 * l << '\n';
    } else cout << "-1 -1\n";
}

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    cout << setprecision(15) << fixed;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}
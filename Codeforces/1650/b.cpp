void solve() {
    int l, r, a; cin >> l >> r >> a;
    int r1 = max(a * (r / a) - 1, l);
    cout << max(r / a + r % a,  + r1 / a + r1 % a) << '\n';
}

int main() {
    vamos;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}
void solve() {
    int n; cin >> n;
    n -= 3;
    int r = n % 3;
    cout << (n / 3) + 1 + r / 2 << " " << (n / 3) + 2 + (r + 1) / 2 << " " << (n / 3) << '\n';
}

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    cout << setprecision(15) << fixed;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}
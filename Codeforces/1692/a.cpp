void solve() {
    int a, b, c, d; cin >> a >> b >> c >> d;
    int cnt = 0;
    for (auto &u : {b, c, d}) {
        cnt += a < u;
    }
    cout << cnt << '\n';



}

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    cout << setprecision(15) << fixed;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}
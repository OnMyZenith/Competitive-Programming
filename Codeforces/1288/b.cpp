#include "bits/stdc++.h"
using namespace std;
using ll = long long;

#ifdef asr_debug
#include "dbg.hpp"
#else
#define dbg(...) 007
#endif


void solve() {
    ll a, b; cin >> a >> b;
    ll cnt = 0, _b = 9;
    while (_b <= b) {
        _b *= 10;
        _b += 9;
        cnt++;
    }
    cout << a * cnt << '\n';
}

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    cout << setprecision(15) << fixed;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}
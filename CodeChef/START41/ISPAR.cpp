void solve() {
    ll n, k; cin >> n >> k;
    bool even = 0;
    if(k == 1) even = !(n & 1);
    else {
        even = (k != 2);
    }

    cout << (even ? "EVEN" : "ODD") << '\n';



}

int main() {
    vamos;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}
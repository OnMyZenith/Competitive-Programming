void solve() {
    int x, y; cin >> x >> y;
    int ans = 0;
    if(y >= x) ans = y - x;
    else {
        ans = ((x - y) + 1) / 2 +  ((x - y) & 1);
    }
    cout << ans << '\n';



}

int main() {
    vamos;

    int TT = 1;
    cin >> TT;
    while(TT--) {
        solve();
    }

    return 0;
}